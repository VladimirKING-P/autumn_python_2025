#todo: Дана сторона квадрата a. Найти его площадь S = a²
# Примечание: сторону квадрата получаем через функцию input().


a = input("Введите сторону квадрата: ")
a = int(a)  # Если сторона квадрата - целое число, используем int(). Если с плавающей точкой, используем float().
S = a ** 2
print("Площадь квадрата:", S)
